public class ConversorPuntoALienzo {

    public static float obtenerMargenX(float x){
        return x * 0.10f;
    }
    
    public static float obtenerMargenY(float y){
        return y * 0.05f;
    }
    
    public static float obtenerLargoEjeX(float x){
        return x * 0.7f;
    }
    
    public static float obtenerLargoEjeY(float y){
        return y * 0.85f;
    }
}
